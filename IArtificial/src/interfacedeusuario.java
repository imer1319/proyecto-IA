     String resultado="";
     resultado=se.ObtenerCalzado((String)jComboBox1.getSelectedItem(),(String)jComboBox2.getSelectedItem());
     if(resultado=="TieneZapatillas"){
            tienepico tp=new tienepico();
            tp.show();
            }
